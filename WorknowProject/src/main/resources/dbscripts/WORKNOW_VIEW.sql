CREATE VIEW PERSONAL_VIEW (MEMBER_TYPE, MEMBER_ID,MEMBER_NAME,MEMBER_PHONE,MEMBER_EMAIL,
PER_TALK,PER_AGE,PER_ADDRESS,PER_SEX,PER_PHOTO)
AS(
SELECT MEMBER_TYPE, MEMBER_ID,MEMBER_NAME,MEMBER_PHONE,MEMBER_EMAIL,
PER_TALK,PER_AGE,PER_ADDRESS,PER_SEX,PER_PHOTO FROM
(SELECT MEMBER_TYPE, MEMBER_ID,MEMBER_NAME,MEMBER_PHONE,MEMBER_EMAIL FROM MEMBER_INFO) 
INNER JOIN (SELECT PER_ID,PER_TALK,PER_AGE,PER_ADDRESS,PER_SEX,PER_PHOTO FROM PERSONAL_INFO)
ON MEMBER_ID= PER_ID
)


-- Table 명		PERSONAL_VIEW				개인이 내정보 볼때 보여줄것 (1번빼고 ) + 구인검색에서 이력서 정보 보여줄때 사용			
