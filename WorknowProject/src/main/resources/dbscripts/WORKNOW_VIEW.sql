CREATE VIEW COMPANY_VIEW (MEMBER_TYPE, MEMBER_ID,MEMBER_NAME,MEMBER_PHONE,MEMBER_EMAIL,
COM_NUMBER,COM_NAME,COM_ADDRESS,COM_CEO,COM_PHONE)
AS(
SELECT MEMBER_TYPE, MEMBER_ID,MEMBER_NAME,MEMBER_PHONE,MEMBER_EMAIL,
COM_NUMBER,COM_NAME,COM_ADDRESS,COM_CEO,COM_PHONE FROM
(SELECT MEMBER_TYPE, MEMBER_ID,MEMBER_NAME,MEMBER_PHONE,MEMBER_EMAIL FROM MEMBER_INFO) 
INNER JOIN (SELECT COM_NUMBER,COM_NAME,COM_ADDRESS,COM_CEO,COM_PHONE,COM_ID FROM COM_INFO)
ON MEMBER_ID= COM_ID
)
SELECT * FROM  COMPANY_VIEW;
