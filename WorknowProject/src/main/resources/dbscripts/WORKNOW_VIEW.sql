--1	MEMBER_TYPE	타입코드	NUMBER	N	PK	0 개인회원 1 기업회원 (쓸지말지 생각중)			
--2	MEMBER_ID	아이디	VARCHAR2(100)	N	PK	회원번호랑 같은것으로 부여			
--3	MEMBER_NAME	이름	VARCHAR2(100)	N		이름			
--4	MEMBER_PHONE	폰번호	VARCHAR2(200)	N					
--5	MEMBER_EMAIL	이메일	VARCHAR2(100)	N					
--6	PER_ID	개인회원id	VARCHAR2(100)	N	FK(MEMBER_ID)	회원id에 대한키			
--7	PER_TALK	개인소갯말	VARCHAR2(400)	N		100자 이내 간단한 소갯말			
--8	PER_AGE	나이	NUMBER	N					
