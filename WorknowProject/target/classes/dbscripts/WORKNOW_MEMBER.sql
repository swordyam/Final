DROP TABLE MEMBER_INFO;
CREATE TABLE MEMBER_INFO(
MEMBER_ID VARCHAR2(100) NOT NULL,
MEMBER_PASS VARCHAR2(100) NOT NULL,
MEMBER_NAME VARCHAR2(100) NOT NULL,
MEMBER_PHONE VARCHAR2(100) NOT NULL,
MEMBER_EMAIL VARCHAR2(200) NOT NULL,
MEMBER_TYPE NUMBER NOT NULL,
MEMBER_DATE	DATE NOT NULL,
MEMBER_BLACK NUMBER NOT NULL,
CONSTRAINT MEMBER_ID_PK PRIMARY KEY (MEMBER_ID)
)


-- Table 명		MEMBER_INFO							
--No	Column 명	한글명 	자료형	NULL	Key	비고 / 설명			
--1	MEMBER_ID	아이디	VARCHAR2(100)	N	PK	회원번호랑 같은것으로 부여			
--2	MEMBER_PASS	비밀번호	VARCHAR2(100)	N					
--3	MEMBER_NAME	이름	VARCHAR2(100)	N		이름			
--4	MEMBER_PHONE	폰번호	VARCHAR2(200)	N					
--5	MEMBER_EMAIL	이메일	VARCHAR2(100)	N					
--6	MEMBER_TYPE	회원분류	NUMBER	N		0 개인회원 1 기업회원
--7	MEMBER_DATE	가입날짜	DATE	N		가입날짜 SYSDATE로 삽입			
--8	MEMBER_BLACK	회원블랙리스트	NUMBER	N	디폴트 0	신고횟수에 따른 블랙리스트점수(일정숫자 이상시 제제)			

